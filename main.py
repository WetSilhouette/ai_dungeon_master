def apply_stat_updates(stats: dict, updates: dict) -> dict:
    for key in ("hp", "attack", "defence", "gold"):
        delta = updates.get(key)
        if isinstance(delta, (int, float)):
            stats[key] = stats.get(key, 0) + delta

    inventory_updates = updates.get("inventory")
    if isinstance(inventory_updates, dict):
        inventory = stats.setdefault("inventory", [])
        for item in inventory_updates.get("remove") or []:
            if item in inventory:
                inventory.remove(item)
        inventory.extend(inventory_updates.get("add") or [])

    stats["hp"] = max(0, min(stats["hp"], stats["max_hp"]))
    stats["gold"] = max(0, stats["gold"])
    return stats


def match_move_action(action_text: str, exits: list) -> Optional[dict]:
    """Check if the submitted action names one of the current location's exits."""
    lowered = action_text.lower()
    for exit_id, exit_name in exits:
        if exit_name.lower() in lowered:
            return {"id": exit_id, "name": exit_name}
    return None


def ensure_move_action(actions: List[str], exits: list) -> List[str]:
    """Guarantee one of the offered actions is a move option whenever exits exist."""
    if not exits:
        return actions
    exit_names_lower = [name.lower() for _, name in exits]
    if any(exit_name in a.lower() for a in actions for exit_name in exit_names_lower):
        return actions
    _, chosen_name = random.choice(exits)
    updated = list(actions)
    updated[-1] = f"Move to {chosen_name}"
    return updated


def location_to_dict(location_row) -> dict:
    return {"id": location_row[0], "name": location_row[1], "description": location_row[2], "atmosphere": location_row[3]}


async def check_achievements(game_id: str, ctx: dict) -> List[str]:
    """Award any achievements newly satisfied by the current game state. Each is awarded once per game."""
    earned_ids = {row[0] for row in await get_earned_achievements(game_id)}
    earned_at = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    newly_earned = []
    for achievement in ACHIEVEMENTS:
        if achievement["id"] in earned_ids:
            continue
        if achievement["check"](ctx):
            await award_achievement(game_id, achievement["id"], earned_at)
            newly_earned.append(achievement["name"])
    return newly_earned


def detect_combat_trigger(text: str) -> bool:
    lowered = text.lower()
    return any(trigger in lowered for trigger in COMBAT_TRIGGER_WORDS)


async def game_has_ended(game_id: str) -> bool:
    """A game is over on death, at the turn cap, or once manually ended via /end."""
    if await is_game_ended(game_id):
        return True
    turn_row = await get_last_turn(game_id)
    stats = ast.literal_eval(turn_row[5])
    turn_number = turn_row[2]
    return stats["hp"] <= 0 or turn_number >= MAX_TURNS


def roll_damage(attack: int) -> int:
    return random.randint(attack - 5, attack + 5)


def resolve_combat_round(action_text: str, stats: dict, enemy: dict) -> dict:
    """Resolve one round of combat with dice rolls - the LLM is not involved in the math."""
    action_lower = action_text.lower()
    summary_parts = []
    defending = False

    if "flee" in action_lower or "run" in action_lower or "escape" in action_lower:
        if random.random() < 0.4:
            summary_parts.append(f"You successfully flee from the {enemy['name']}.")
            return {"outcome": "fled", "summary": " ".join(summary_parts), "stats": stats, "enemy": enemy}
        summary_parts.append(f"You try to flee, but the {enemy['name']} blocks your escape.")

    elif "defend" in action_lower or "block" in action_lower or "guard" in action_lower:
        defending = True
        summary_parts.append(f"You raise your guard against the {enemy['name']}.")

    else:
        damage = max(0, roll_damage(stats["attack"]))
        enemy["hp"] = max(0, enemy["hp"] - damage)
        summary_parts.append(f"You attack the {enemy['name']} for {damage} damage.")
        if enemy["hp"] <= 0:
            summary_parts.append(f"The {enemy['name']} is defeated!")
            return {"outcome": "victory", "summary": " ".join(summary_parts), "stats": stats, "enemy": enemy}

    # Enemy retaliates - reached after a failed flee, a defend, or a non-lethal attack
    defence = stats["defence"] * (2 if defending else 1)
    damage = max(0, roll_damage(enemy["attack"]) - defence)
    stats["hp"] = max(0, stats["hp"] - damage)
    summary_parts.append(f"The {enemy['name']} strikes back for {damage} damage.")

    outcome = "defeat" if stats["hp"] <= 0 else "ongoing"
    if outcome == "defeat":
        summary_parts.append("You collapse, defeated.")

    return {"outcome": outcome, "summary": " ".join(summary_parts), "stats": stats, "enemy": enemy}


