@app.post("/games", response_model=Game)
async def create_game(game: GameInput):
    new_game_id = "".join([random.choice(alphabet_list) for _ in range(10)])

    game_inputs = GameInput(
        player_name=game.player_name,
        setting=game.setting
    )

    location = location_to_dict(await get_location(STARTING_LOCATION_ID))
    exits = await get_location_exits(STARTING_LOCATION_ID)

    game_output = await start_game(game_inputs, location=location)
    actions = ensure_move_action(game_output["actions"], exits)
    await save_game(new_game_id, game_inputs)

    location_state = {"current_location": STARTING_LOCATION_ID, "visited_locations": [STARTING_LOCATION_ID]}
    await update_location_state(new_game_id, json.dumps(location_state))

    new_game = Game(
        game_id=new_game_id,
        scene=game_output["scene"],
        actions=actions,
        stats=Stats(**DEFAULT_STATS),
        location=LocationInfo(
            current_location=location["name"],
            visited_locations=location_state["visited_locations"],
            available_exits=[name for _, name in exits]
        )
    )

    # Create character in "turns" table in database - no action has been taken yet
    await take_action_db(new_game_id, "", result=new_game.scene, stats=f"{DEFAULT_STATS}", turn_number=1,
                          actions=json.dumps(new_game.actions))

    return new_game


@app.post("/games/{game_id}/action", response_model=Turn)
async def take_action(game_id: str, action: TurnInput):

    game_history = await get_game_history(game_id)
    previous_turn_number = game_history[0][2]

    # Record the player's chosen action against the turn it responds to
    await update_action(game_id=game_id, action=action.action, turn_number=previous_turn_number)

    stats_row = await get_last_turn(game_id)
    stats = ast.literal_eval(stats_row[5])

    combat_row = await get_combat_state(game_id)
    combat_state = json.loads(combat_row[0]) if combat_row and combat_row[0] else {"in_combat": False}

    location_row = await get_location_state(game_id)
    location_state = json.loads(location_row[0]) if location_row and location_row[0] else {
        "current_location": STARTING_LOCATION_ID, "visited_locations": [STARTING_LOCATION_ID]
    }
    current_location_id = location_state["current_location"]

    turn_number = previous_turn_number + 1

    if combat_state.get("in_combat"):
        enemy = combat_state["enemy"]
        round_result = resolve_combat_round(action.action, stats, enemy)
        stats = round_result["stats"]
        enemy = round_result["enemy"]
        outcome = round_result["outcome"]

        result_text = await narrate_combat(round_result["summary"])

        if outcome == "victory":
            combat_state = {"in_combat": False}
            await increment_combat_wins(game_id)
            actions = ["Search the fallen enemy for loot", "Catch your breath and take stock of your surroundings", "Press onward"]
        elif outcome == "fled":
            combat_state = {"in_combat": False}
            actions = ["Put some distance between yourself and the area", "Catch your breath", "Look for another way forward"]
        elif outcome == "defeat":
            combat_state = {"in_combat": False}
            actions = []
        else:
            combat_state = {"in_combat": True, "enemy": enemy}
            actions = ["Attack", "Defend", "Flee"]

        await update_combat_state(game_id, json.dumps(combat_state))

        game_over = stats["hp"] <= 0 or turn_number >= MAX_TURNS
        stats_str = str(stats)
        await take_action_db(game_id=game_id, action="", result=result_text, turn_number=turn_number, stats=stats_str,
                              actions=json.dumps(actions))

        current_location = location_to_dict(await get_location(current_location_id))
        current_exits = await get_location_exits(current_location_id)

        achievements_earned = await check_achievements(game_id, {
            "combat_wins": await get_combat_wins(game_id),
            "stats": stats,
            "visited_locations": location_state["visited_locations"],
            "turn": turn_number
        })

        return Turn(
            result=result_text,
            actions=actions,
            turn=turn_number,
            stats=Stats(**stats),
            game_over=game_over,
            combat=CombatInfo(
                in_combat=combat_state["in_combat"],
                enemy_name=enemy["name"] if combat_state["in_combat"] else None,
                enemy_hp=enemy["hp"] if combat_state["in_combat"] else None
            ),
            location=LocationInfo(
                current_location=current_location["name"],
                visited_locations=location_state["visited_locations"],
                available_exits=[name for _, name in current_exits]
            ),
            achievements_earned=achievements_earned
        )

    # Not in combat - normal story flow. Re-fetch history so the LLM sees the action just recorded above
    game_history = await get_game_history(game_id)

    exits_before_move = await get_location_exits(current_location_id)
    move_target = match_move_action(action.action, exits_before_move)
    arriving = move_target is not None
    if arriving:
        current_location_id = move_target["id"]
        location_state["current_location"] = current_location_id
        if current_location_id not in location_state["visited_locations"]:
            location_state["visited_locations"].append(current_location_id)

    current_location = location_to_dict(await get_location(current_location_id))
    current_exits = await get_location_exits(current_location_id)

    response = await continue_game(game_history, stats=stats, location=current_location, arriving=arriving)

    updates = response.get("updates")
    if isinstance(updates, dict):
        gold_delta = updates.get("gold")
        if isinstance(gold_delta, (int, float)) and gold_delta > 0:
            await increment_gold_collected(game_id, int(gold_delta))
        stats = apply_stat_updates(stats, updates)
    game_over = stats["hp"] <= 0 or turn_number >= MAX_TURNS
    stats_str = str(stats)

    combat_start = response.get("combat")
    triggered = isinstance(combat_start, dict) and isinstance(combat_start.get("enemy"), dict)
    if not triggered and detect_combat_trigger(response["result"]):
        triggered = True
        combat_start = {"enemy": DEFAULT_ENEMY.copy()}

    enemy = None
    if triggered and not game_over:
        enemy_data = combat_start["enemy"]
        enemy = {
            "name": enemy_data.get("name", DEFAULT_ENEMY["name"]),
            "hp": int(enemy_data.get("hp", DEFAULT_ENEMY["hp"])),
            "attack": int(enemy_data.get("attack", DEFAULT_ENEMY["attack"])),
        }
        combat_state = {"in_combat": True, "enemy": enemy}
        actions = ["Attack", "Defend", "Flee"]
    else:
        combat_state = {"in_combat": False}
        actions = [] if game_over else ensure_move_action(response["actions"], current_exits)

    await update_combat_state(game_id, json.dumps(combat_state))
    await update_location_state(game_id, json.dumps(location_state))

    # This turn's action is unknown until the player's next call updates it
    await take_action_db(game_id=game_id, action="", result=response["result"], turn_number=turn_number, stats=stats_str,
                          actions=json.dumps(actions))

    achievements_earned = await check_achievements(game_id, {
        "combat_wins": await get_combat_wins(game_id),
        "stats": stats,
        "visited_locations": location_state["visited_locations"],
        "turn": turn_number
    })

    turn = Turn(
        result=response["result"],
        actions=actions,
        turn=turn_number,
        stats=Stats(**stats),
        game_over=game_over,
        combat=CombatInfo(
            in_combat=combat_state["in_combat"],
            enemy_name=enemy["name"] if enemy else None,
            enemy_hp=enemy["hp"] if enemy else None
        ),
        location=LocationInfo(
            current_location=current_location["name"],
            visited_locations=location_state["visited_locations"],
            available_exits=[name for _, name in current_exits]
        ),
        achievements_earned=achievements_earned
    )
    return turn


@app.post("/games/{game_id}/save", response_model=SaveResponse)
async def save_game_endpoint(game_id: str):
    if await get_game(game_id) is None:
        raise HTTPException(status_code=404, detail="No game found with that id")

    saved_at = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    for _ in range(5):
        save_code = generate_save_code()
        try:
            await create_save(game_id, save_code, saved_at)
            return SaveResponse(save_code=save_code)
        except sqlite3.IntegrityError:
            continue

    raise HTTPException(status_code=500, detail="Could not generate a unique save code, please try again")


@app.post("/games/load", response_model=LoadedGame)
async def load_game(load_input: LoadInput):
    save_row = await get_save(load_input.save_code)
    if save_row is None:
        raise HTTPException(status_code=404, detail="No game found with that code")

    game_id = save_row[0]
    turn_row = await get_last_turn(game_id)
    stats = ast.literal_eval(turn_row[5])
    actions = json.loads(turn_row[6])

    combat_row = await get_combat_state(game_id)
    combat_state = json.loads(combat_row[0]) if combat_row and combat_row[0] else {"in_combat": False}
    enemy = combat_state.get("enemy")

    location_row = await get_location_state(game_id)
    location_state = json.loads(location_row[0]) if location_row and location_row[0] else {
        "current_location": STARTING_LOCATION_ID, "visited_locations": [STARTING_LOCATION_ID]
    }
    current_location = location_to_dict(await get_location(location_state["current_location"]))
    exits = await get_location_exits(location_state["current_location"])

    return LoadedGame(
        game_id=game_id,
        scene=turn_row[4],
        actions=actions,
        stats=Stats(**stats),
        turn=turn_row[2],
        combat=CombatInfo(
            in_combat=combat_state.get("in_combat", False),
            enemy_name=enemy["name"] if enemy else None,
            enemy_hp=enemy["hp"] if enemy else None
        ),
        location=LocationInfo(
            current_location=current_location["name"],
            visited_locations=location_state["visited_locations"],
            available_exits=[name for _, name in exits]
        )
    )


